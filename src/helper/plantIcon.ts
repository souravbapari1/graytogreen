export function getTreeStateColor(state: string): string {
  switch (state) {
    case "new planted":
      return "#add498"; // Green for newly planted trees
    case "good":
      return "#24d430"; // Light Green for trees in good condition
    case "poor":
      return "#f9e86a"; // Amber for trees that are poor
    case "dead":
      return "#636363"; // Gray for dead trees
    case "producing":
      return "#f08411"; // Orange for trees that are producing
    case "not planted":
      return "#ffc9dc"; // Red for areas with no trees planted
    default:
      return "#e36d6d"; // Default to black if an unknown state is provided
  }
}

